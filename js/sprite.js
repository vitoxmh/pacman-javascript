    constructor(ctx, stageCtx = null){

        this.ctx = ctx;
        this.stageCtx = stageCtx;
        this.width = 2;
        this.paddingTop = 50;

        this.spriteCache = new Map();
        this.tileCache = new Map();
        
        this._colorArray = new Array(20);
        for(let i = 0; i < 20; i++) {
            this._colorArray[i] = this.color[i] || '#000';
        }

    }

    renderStageToBuffer(stage) {
        if (!this.stageCtx || !stage) return;
        
        this.stageCtx.fillStyle = "#000";
        this.stageCtx.fillRect(0, 0, 500, 600);
        
        for (let y = 0; y < stage.length; y++) {
            for (let x = 0; x < stage[y].length; x++) {
                const value = stage[y][x];
                if (value !== 0) {
                    const _x = x * 8 * this.width;
                    const _y = y * 8 * this.width;
                    this.renderSpriteToBuffer(_x, _y, this.getTiled(value));
                }
            }
        }
    }

    renderSpriteToBuffer(dx, dy, sprites) {
        if (!sprites || !sprites[0] || !this.stageCtx) return;
        
        const rows = sprites.length;
        const cols = sprites[0].length;
        const spriteWidth = cols * this.width;
        const spriteHeight = rows * this.width;
        
        const colorMap = new Map();
        
        for (let y = 0; y < rows; y++) {
            const row = sprites[y];
            for (let x = 0; x < cols; x++) {
                const value = row[x];
                if (value === 0) continue;
                
                if (!colorMap.has(value)) {
                    colorMap.set(value, []);
                }
                colorMap.get(value).push({x: x * this.width + dx, y: y * this.width + dy});
            }
        }
        
        for (const [colorIndex, pixels] of colorMap) {
            this.stageCtx.fillStyle = this._colorArray[colorIndex];
            this.stageCtx.beginPath();
            for (const p of pixels) {
                this.stageCtx.rect(p.x, p.y, this.width, this.width);
            }
            this.stageCtx.fill();
        }
    }


    getSprite(name,frame = -1){

        if(frame === -1){
            return this.SPRITES[name];
        }
        return this.SPRITES[name][frame];
    
    }

    getTiled(tilded){


        return this.TILE_PATTERNS[tilded];

    }


    

    renderSprite(dx,dy,sprites,rotate = 0,reverse = false, flash = false){

        if (!sprites || !Array.isArray(sprites) || !sprites[0] || !Array.isArray(sprites[0])) {
            return;
        }

        const rows = sprites.length;
        const cols = sprites[0].length;
        const spriteWidth = cols * this.width;
        const spriteHeight = rows * this.width;
        
        const needsTransform = rotate !== 0 || reverse;
        
        if (needsTransform) {
            const cx = dx + spriteWidth / 2;
            const cy = dy + spriteHeight / 2;

            this.ctx.save();
            this.ctx.translate(cx, cy+this.paddingTop);
            this.ctx.rotate(rotate);
            this.ctx.translate(-spriteWidth / 2, -spriteHeight / 2);

            if (reverse) {
                this.ctx.scale(-1, 1);
                this.ctx.translate(-spriteWidth, 0);
            }

            this.drawSpritePixels(sprites, flash);

            this.ctx.restore();
        } else {
            const startX = dx + spriteWidth / 2;
            const startY = dy + spriteHeight / 2 + this.paddingTop;
            
            this.drawSpritePixels(sprites, flash, startX - spriteWidth / 2, startY - spriteHeight / 2);
        }
    }

    drawSpritePixels(sprites, flash, offsetX = 0, offsetY = 0) {
        const colorMap = new Map();
        
        for (let y = 0; y < sprites.length; y++) {
            const row = sprites[y];
            for (let x = 0; x < row.length; x++) {
                let value = row[x];
                if (value === 0) continue;
                
                let colorIndex = value;
                if (flash === 1 && (value === 1 || value === 2)) {
                    colorIndex = 5;
                } else if (flash === 2 && value === 47) {
                    continue;
                } else if (flash === 3 && value === 47) {
                    colorIndex = 5;
                } else if (flash === 4 && (value === 12 || value === 13)) {
                    colorIndex = 5;
                } else if (flash === 5 && value === 12) {
                    colorIndex = 5;
                } else if (flash === 5 && value === 13) {
                    colorIndex = 0;
                }
                
                if (!colorMap.has(colorIndex)) {
                    colorMap.set(colorIndex, []);
                }
                colorMap.get(colorIndex).push({x: x * this.width + offsetX, y: y * this.width + offsetY});
            }
        }
        
        for (const [colorIndex, pixels] of colorMap) {
            this.ctx.fillStyle = this._colorArray[colorIndex];
            this.ctx.beginPath();
            for (const p of pixels) {
                this.ctx.rect(p.x, p.y, this.width, this.width);
            }
            this.ctx.fill();
        }
    }



    renderStage(stage, flashLevel = 0, flashPellets = 0, flashPowerPellets = 0, intro = false, flashColor12 = false){

        if (!stage) return;
        
        const needsFullRender = flashLevel !== 0 || flashPellets !== 0 || flashPowerPellets !== 0 || intro;
        
        if (!needsFullRender && this.stageCtx && this.stageCanvas) {
            this.ctx.drawImage(this.stageCanvas, 0, 0);
            return;
        }

        stage.forEach((row,y) => {

            row.forEach((value,x) => {
              
                let _x = x * 8 * this.width;
                let _y = y * 8 * this.width;
                if(value != 0){
                    if (intro && (value === 46 || value === 47)) return;
                    if (flashLevel === 1 && (value >= 1 && value <= 45)) {
                        this.renderSprite(_x, _y, this.getTiled(value), 0, false, 1);
                    } else if (flashLevel === 5 && (value >= 1 && value <= 45)) {
                        this.renderSprite(_x, _y, this.getTiled(value), 0, false, 5);
                    } else if (flashPellets === 2 && value === 47) {
                        this.renderSprite(_x, _y, this.getTiled(value), 0, false, 2);
                    } else if (flashPowerPellets === 3 && value === 47) {
                        this.renderSprite(_x, _y, this.getTiled(value), 0, false, 3);
                    } else if (flashColor12 && (value >= 1 && value <= 45)) {
                        this.renderSprite(_x, _y, this.getTiled(value), 0, false, 4);
                    } else if (intro && (value >= 1 && value <= 45)) {
                        return;
                    } else {
                        this.renderSprite(_x, _y, this.getTiled(value));
                    }
                }
      
            });

        });

    }


    renderText(text,dx,dy,colorIndex = 5){

        let x = dx;
        let y = dy;

        for(let char of text){

            let sprite = this.TEXT[char];

            if (!sprite) continue;

            const spriteColor = sprite.map(fila =>
                fila.map(valor => valor === 3 ? colorIndex : valor)
            );


            if(sprite){
                this.renderSprite(x,y,spriteColor);
            }
            x += (sprite[0].length + 1) * this.width;
        }
    }




      


